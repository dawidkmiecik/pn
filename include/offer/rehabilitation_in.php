			<p>
				<strong>Istotnym</strong> elementem procesu leczenia chorych w podeszłym wieku jest rehabilitacja, której głównym celem jest utrzymanie lub przywrócenie zdolności do samodzielnego funkcjonowania, szczególnie w zakresie podstawowych czynności życia codziennego, jak również usprawnianie poszczególnych narządów czy układów, w których występuje dysfunkcja. Pod pojęciem rehabilitacji rozumiemy także uzdolnienie do życia z wykorzystaniem wszystkich potencjalnych możliwości pomimo pogorszenia się stanu zdrowia. Pacjent motywowany jest do odpowiedzialności i działania. Oczywiście przez cały czas należy pamiętać, że jest to człowiek cierpiący, który wymaga wszelkiej możliwej pomocy ze strony rodziny i opiekunów. Poprawa stanu zdrowia może nastąpić, gdy pacjent mobilizuje własne siły i własną wolę.
				<br />
				<br />
				</p> 
				<p class="comment">
				<strong>Podmiotem</strong> rehabilitacji geriatrycznej jest pacjent w zaawansowanym wieku, cierpiący zazwyczaj na klika chorób przewlekłych, które wzajemnie na siebie wpływają i stanowią zagrożenie dla samodzielności pacjenta. Jej formami są: rehabilitacja prewencyjna zmierzająca do utrzymania dotychczasowych kompetencji seniora w celu jak najdłuższego unikania chorób, niesprawności i niesamodzielności, ogólna skierowana do seniorów przewlekle chorych, polegająca na ich uaktywnianiu oraz celowa dopasowana do poszczególnych jednostek chorobowych i potrzeb pacjentów.
