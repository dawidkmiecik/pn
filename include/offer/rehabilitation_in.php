			</h3>
			<p>
				Terapia reumatyzmu ma charakter kompleksowy i obejmuje indywidualny, w zależności od stopnia rozwoju choroby, dobór środków farmakologicznych, dietę oraz cały zespół metod rehabilitacyjnych. Leki zmniejszają wytwarzanie przez organizm mediatorów zapalenia, a więc substancji wywołujących chorobę oraz hamują ich uwalnianie. <strong>Konieczna jest też rehabilitacja ruchowa,</strong> a więc układ ćwiczeń, bez których stawy ulegają coraz większym zniekształceniom. Brzmi to jak paradoks, ale to ruch, mimo że sprawia ból, powstrzymuje rozwój choroby. Jego brak wpływa niekorzystnie na odżywianie chrząstki stawowej i wytwarzanie specjalnego płynu przez błonę maziową, którego zadaniem jest zmniejszanie tarcia między elementami tworzącymi staw.
			</p>

